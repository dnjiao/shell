package models;

public class VolPricePair {
	private double volume;
	private double price;
	public VolPricePair() {
		super();
	}
	public double getVolume() {
		return volume;
	}
	public void setVolume(double volume) {
		this.volume = volume;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	

}
